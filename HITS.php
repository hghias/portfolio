    <h4> <b> The Problem </b></h4>
    <p>Health Information Technology & Services (HITS) provides Michigan Medicine employees with technology-related information, products, services, and support. The Service Portal is an online platform that aims to make requesting these products and services quick and easy. However we found that in the last 12 months, 42% of calls and general requests could've been resolved through the service portal. I was brought to the team to understand how customers are using the Service Portal, how successful they are in finding what they're looking for, and what design changes can improve efficiency. Through RITE (Rapid Iterative Testing and Evaluation) testing, I created five prototypes and measured task success rates in each prototype and worked with the project manager to ensure technical feasibility of the redesign.  </p>

