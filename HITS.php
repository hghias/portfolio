    <h4> <b> Business Goals </b></h4>
    <p>In order to identify the research questions, I talked to stakeholders to define our business goals:</p>

    <div class="pl-3">
        <ul>
            <li style="font-size: 26px;">Reduce the number of calls to HITS Support desk for service catalog requests and redirect customers to request items from the Service Portal</li>
            <li style="font-size: 26px;">Reduce the percentage of service catalog requests created through the general request</li>
        </ul>
       
    </div>

     </br>

    <h4> <b> Research Questions </b></h4>
    <p>To generate insights into our customer population, I designed the research process with a goal of better understanding:</p>
    <ul>
        <li style="font-size: 20px;"> What do customers know about the support we offer?</li>
        <li style="font-size: 20px;">How can we​ <b>increase customer awareness​</b> of the Service Portal?</li>
        <li style="font-size: 20px;">What is the ​<b>willingness to leverage​ the service portal</b> once customers are aware of it? How can we motivate customers to use the Service Catalog before they call the Service Desk or submit a general request? </li>
        <li style="font-size: 20px;"> How do customers expect the process of seeking and receiving help from HITS to work? </li>
        <li style="font-size: 20px;"> Does the current Service Portal fulfill ​<b>customers’ expectations​ </b> of how a self-help, online portal should function? What ​<b> pain points​ </b> can we identify in the current workflow? </li> 
    </ul>
    </br>

    <h5 style="color: #777777"> <b>Research Plan</b> </h5>
    <hr>
    <h4> <b> Usability Testing Protocol </b></h4>

    <p>To investigate the research questions, I conducted one-on-one interviews consisting of two parts: questions to <b>understand their role at Michigan Medicine and experience with HITS</b> and a usability test to observe their <b>success rate navigating through the Service Portal.</b> I conducted interviews with users one-on-one, meeting each person in their place of work and once Michigan went into lockdown during the Coronavirus epidemic, we met through video conferencing.</p>


    <p></p>
    <p>Each interview lasted 30 minutes to 1 hour, and followed the same format:</p>
    <ul>
        <li>Introduce the purpose of the interview and seek verbal consent from the interview participant to participate and make audio and screen recording.</li>
        <li> Ask about participant’s role and technological needs</li>
        <li>Ask about how participant approaches solving a technological problem </li>
        <li>Ask about participant’s experience with HITS and how they would rank thedifferent ways to contact HITS. </li>
        <li>Provide participant with four tasks: </li>
        <ul>
            <li>Request Adobe Acrobat Pro</li>
            <li>Get a printer installed on your machine</li>
            <li>Microsoft Word keeps crashing. How do you go about fixing it? </li>
            <li>Request a Display</li>
        </ul>
        <li>Ask about their experience using the Service Portal</li>
    </ul>

    </br>
    <h4> <b> Recruiting Methods </b></h4>
    <p>We emailed a survey to Michigan Medicine employees who had previously contacted HITS to recruit interviewees. Out of the 170 responses, we identified 35 people who represented diverse characteristics such as job role, preferred method of receiving IT help, and experience using existing U-M IT websites. Of those 35, we received responses from and interviewed 12 people.</p>
    <p> <b>Limitations of our sample: </b> We did not get a chance to interview any students, or residents or fellows. Due to how busy nurses and clinicians were during the Coronavirus epidemic, we did not get enough participants from that population.</p>
    </br>
    </br>

