    <h5> <b> Key Findings </b></h5>
    <ul>
        <li>How customer contacts HITS depends on the urgency and complexity of the problem</li>
        <li> Most prefer to
            <ol>
                <li> go to their onsite tech help </li>
                <li> call or chat </li>
                <li> Submit a general ticket</li>
            </ol>
        </li>
        <li>One customer was familiar with the service portal, but no one had used the service catalog before</li>
        <li>Hesitance to use service catalog because: 
            <ul>
                <li> <i>“Without you showing me this was the right way to go and that I’m ordering the right thing, I’d be a little leery [to use the service catalog to order something]”</i> </li>
                <li> They are not sure if they need approval from a manager/admin and want to know who’s getting charged </li>
            </ul>
        </li>
        <li>Categories & Products
            <ul>
                <li> Customers had some trouble navigating through the catalog categories </li>
                <li> Confusion on whether a software is free or not </li>
            </ul>
        </li>
        <li>Search
            <ul>
                <li> Some customers went straight to search, others were hesitant because they didn’t expect to find what they were looking for </li>
                <li> Pleasantly surprised to see that search results are easy to navigate </li>
                <li> Some customers used search for non-catalog items </li>
            </ul>
        </li>
        <li>Tours
            <ul>
                <li> Some found it helpful, others did not like how thorough it was </li>
            </ul>
        </li>
    </ul>

