	<h5> <b>Secondary Research</b> </h5>
	<a href="https://dentistry.uic.edu/patients/oral-health-costs" target="blank" style="color:#48A2B5;"> UIC College of Dentistry: The Many Costs of Poor Oral Health </a>
	<ul>
		<li>Poor oral health is linked to other more serious diseases like cardiovascular disease, cancer, birth complications, and other medical issues (UIC, 2019)</li>
	</ul>
	<a href="https://www.cdc.gov/oralhealth/oral_health_disparities/index.htm" target="blank" style="color:#48A2B5;"> CDC: Disparities in Oral Health </a>
	<ul>
		<li>Overall non-Hispanic blacks, Hispanics, and American Indians and Alaska Natives generally have the poorest oral health of any racial and ethnic groups in the United States. (CDC, 2016) </li>
	</ul>
	<a href="https://www.dentalhealth.ie/dentalhealth/causes/socioeconomicst.html" target="blank" style="color:#48A2B5;"> Dental Health Foundation, Ireland: Socio-Economic Status </a>
	<ul>
		<li>“Low socio-economic status was significantly associated with increased oral cancer risk, even after adjusting for potential behavioural confounders”</li>
		<li> Smoking is more common in low SES population (Dental Health Foundation, 2007)</li>
	</ul>

