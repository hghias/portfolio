	<h3 class="text-center"> <b>Ideation</b> </h3>
	<hr>
	<h5> <b>Initial Ideation</b> </h5>
	<p>Based on our affinity diagramming and persona development, we came up with different features we could incorporate into the app.</p>
	<ul>
		<li> Track your oral health behavior (brushing, flossing, etc..) </li>
		<li> Rewards for consistent healthy behavior </li>
		<li> Personalized notifications to encourage/remind healthy behavior </li>
		<li> Factual tidbits delivered through a  notifications </li>
		<li> Symptom Checker </li>
		<li> Find a dentist </li>
		<li> Goal Tracker </li>
		<li> Procedure breakdowns (process, aftercare, risk, cost, behaviors that lead to these issues) </li>
		<li> Music while brushing </li>
		<li> Cooking healthy </li>
		<li> New Blogs/Articles every week </li>
		<li> Location Based healthy restaurant suggestions</li>
	</ul>

	<p>We categorized those features under two main categories: holistic oral health and transparent & accessible healthcare in order to prevent potential diseases since our target group is more exposed to them.</p>
	<table>
		<tr>
			<th>Holistic Oral Health</th>
			<th> Transparent & Accessible Healthcare</th>
		</tr>
		<tr>
			<td>People understand the importance of oral health, but don’t prioritize it. If we can get people to see the link between & long-term effects of their oral health on the rest of their life (mental, physical, social), they’ll incorporate more preventative habits into their lifestyle.</td>
			<td>There isn’t an easy way to digest information for the everyday patient to guide the incorporation of preventative behaviors past brushing and flossing behaviors and to learn about diseases and treatments.</td>
		</tr>
	</table>

