        <h4> <b> Initial Research </b></h4>

        <div class="row">
            <div class="col-lg-6">
                <p> To learn more about wellbeing, I turned to the book <b>Positive Computing</b> by Dorian Peters and Rafael A. Calvo.</p>
                <p>The most interesting model of wellbeing outlined in the book is <b>subjective wellbeing</b> which consists of life satisfaction, the presence of positive mood and the absence of negative mood.</p>
                <p>The different approaches for designing positive computing are a <b>preventative approach,</b> where obstacles to wellbeing are treated as errors and trigger redesign, <b>active approach, </b> where technology is designed to actively support components of wellbeing in an app that has a different overall goal or <b> dedicated approach,</b> where technology dedicated to fostering wellbeing and/or human potential in some way.</p>
                <p> From the wellbeing factors chart, the factors I'm most interested in incorporating positive emotions, motivation & engagement, self-awareness and mindfulness, through a preventative or active positive computing approach.</p>
