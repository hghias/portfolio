        <h4> <b> Initial Research </b></h4>

        <div class="row">
            <div class="col-6">
                <p> To figure out what type of wellbeing I wanted to tackle with my product, I turned to the book Positive Computing: Technology for Wellbeing and Human Potential by Dorian Peters and Rafael A. Calvo.</p>
                <p>The book outlined the different models of wellbeing. The ones that were most interesting to me were:</p>
                <ul>
                    <li> <b> Hedonic Psychology:</b> Wellbeing as the experience of positive emotion </li>
                    <li> <b> Subjective Wellbeing:</b> Consists of life satisfaction, the presence of positive mood and the absence of negative mood. </li>
                    <li> <b> Eudaimonic Psychology:</b> Wellbeing as engagement with meaning and the fulfillment of potentials</li>
                </ul>

                <p>The different approaches to designing positive computing are:</p>
                <ul>
                    <li> <b>None:</b> Wellbeing is not systematically considered in the design of the technology</li>
                    <li><b>Preventative:</b> Obstacles or compromises to wellbeing are treated as errors and trigger redesign</li>
                    <li><b>Active:</b> Technology is designed to actively support components of wellbeing or human potential in an application that has a different overall goal</li>
                    <li> <b>Dedicated:</b> A technology that is purpose built and dedicated to fostering wellbeing and/or human potential in some way.</li>
                </ul>
            </div>
            <div class="col-6">
                <img src="MunchiesResearch.jpg" alt="" style="width:100%; border: 1px solid #DEDDDD;">
            </div>
        </div>
        <p> The wellbeing factors that I'm interested in incorporating into my product are positive emotions, motivation & engagement, self-awareness and mindfulness, through a preventative or active positive computing approach. </p>
