                <h4> <b> The Problem </b></h4>
                <h4 style="line-height:35px;"> Learning how to <b>express emotions in a positive way</b> is difficult for children to learn, but has a huge impact on their emotional maturity and overall health. Since children develop their emotional vocabulary between the age of <b>18 months to 4 years old,</b> they need an engaging way to learn how to label and identify emotions. This is where Munchies comes in handy. Children feed Munchies everyday with the color jellybean that corresponds with how they are feeling that day. This helps children <b>learn to label and identify their emotions</b> and helps parents communicate with their children. Munchies is a cute and approachable monster and by feeding him, the child develops an emotional bond, encouraging consistent behavior. </h4>

