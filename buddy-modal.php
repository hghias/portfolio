	<h3 class="text-center"><b>The Challenge</b></h3>
	<p>
	For my Change By Design class (artdes 314), we had to ride and fully experience a Bird scooter with Beginner’s mind; complete an inventory with documentation as a visual ethnographer; and transform the Bird into the most fantastic experience we could conjure. </p>
	</br>
	<h3 class="text-center"><b>Research</b></h3>
	
	<h5><b>Ethnographic Research</b></h5>
	<p>The bird I ride is parked on the grass. Getting the app set up is not difficult and getting started is pretty self explanatory. The Bird is a small, but sturdy vehicle, but it goes fast which surprised me a lot. I start off slow, but the grass is bumpy and I’m scared I’m going to topple over. I’m standing higher above ground than I expect and as I readjust my feet on the small platform, I get more comfortable and start going faster. When I hit the sidewalk, the ride is a lot smoother and I start enjoying it and speeding up as much as I can with all the pedestrians and bikers around me. As I zoom past people, trying to avoid hitting someone, I hear bits and pieces of conversations, the wind muffling all the sounds around me. I see some friends, and lift one hand to greet them and almost lose control. I keep a death grip on the handles after that. There’s a lot of power in the small scooter and it’s something I didn’t expect when I first got on it – but it’s a welcome surprise. It’s a great vehicle to explore a city and to hang out with friends, but it’s not as reliable to use as transportation. Finding Birds that are charged and close to you is often hard and so you’ll spend more time looking for a bird than it would take you to go to your desired destination by walking or using a bus or bike.</p>
</br>
	<h3 class="text-center"><b>Sketches</b></h3>
	<p>Our favorite idea was to create a platform that would allow two people to ride the scooter. It's not an uncommon sight to see people riding double around campus. It's extremely unsafe and we've seen people get disbalanced and fall off while riding. The platform could solve that problem!</p>
