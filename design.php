		//As a full stack UX designer, I enjoy conducting user research and creating prototypes, but I am most in my element when designing new features to address user needs.
		
	}
	else if (url == "?filter=branding") { //branding
		$('.br').addClass("active");
		$('div.project').hide();
		$('div.branding').fadeIn(1000, "linear");
		$("#header").addClass('underline');

		span.textContent = "Branding";
		caption.textContent= "";
		

	}
	else if (url == "?filter=product") { //product
		$('.pr').addClass("active");

		$('div.project').hide();
		$('div.product').fadeIn(1000, "linear");
		$("#header").addClass('underline');

		span.textContent = "Product";
		caption.textContent= "";
	}
	else { 
		$('.sh').addClass("active"); //show all
		span.textContent = "";
		//caption.textContent= "With a strong drive for solving problems, I take a  multidisciplinary approach to tackling my projects to enhance my work.";
		//With a strong drive for solving problems, I take a  multidisciplinary approach to tackling my projects. I'm always experimenting with how I can bring different mediums together to enhance my work!";
		//I'm a multidisciplinary designer curious about how I can bring different mediums together to enhance my work!
	}
	

	$('.btn').on('click', function(){
	    $('.btn').removeClass("active");
	    $(this).addClass("active");
	}); 

	$(".sh").click(function() { //show all
		$(this).toggleClass("active");
		$('div.project').hide();
		$('div.project').fadeIn(1000, "linear");
		span.textContent = "";
		//caption.textContent= "With a strong drive for solving problems, I take a  multidisciplinary approach to tackling my projects. I'm always experimenting with how I can bring different mediums together to enhance my work!";
	});

