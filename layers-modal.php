	<h3 class="text-center"> <b>The Goal</b> </h3>
	<p>Layers is a campaign that aims to challenge you to be critical of the function of the design around you. Interactive prompts and activities will guide you to understand and apply the Design Thinking process in your daily lives, and analyze the impact of design on society, individuals and environment. 
	A solution-based approach to solving problems, Design thinking relies on the human ability to be intuitive, recognize patterns, and construct ideas that are emotionally meaningful as well as functional. The elements of design thinking combine to form an iterative approach—one you can try out and adapt to suit your needs. First, identify a driving question that inspires you to search for creative solutions, second, empathize with and understand the target audience, third, push past obvious solutions to get to breakthrough ideas, fourth, build rough prototypes to learn how to make ideas better, and lastly refine ideas by gathering feedback and testing solutions.</p>
	<p>I aim to develop a campaign that prompts people to be critical of the function and impact of design around them. Interactive prompts will guide the audience to understand and apply the design thinking process to their daily lives. Through a series of thought-provoking prompts and hands on design challenges, my goal is to get viewers to think about the impact of design among non-designers and in spaces where design would normally go unnoticed. The poster prompts will be placed around campus and distributed through a social media campaign that will link to a website with a way for viewers to share their answers, examples of other peoples' responses, more information about the design principles, and more challenges that they can complete. </p>
	<p>This project stemmed from my passion to use design as a tool to foster inclusivity and create equal opportunity. Gaining inspiration from books like “Design Thinking” by Tim Brown and “The Design of Everyday Things” by Donald A. Norman, Nick Tobier’s Change By Design course and Candy Chang’s “I Wish This Was” interactive street art project, I aim to develop a project that makes design more accessible to people who do not identify as creatives. </p>
	Design is all around us – everything we use has been designed by someone for a specific purpose. Through the use of color, form and the other design elements and principles, designers and companies influence our actions and emotions. In a world where good design is defined by how seamlessly a product is integrated in our lives, it is important to be aware of the design around us and its impact on us so we can keep designers and companies accountable. </p>
