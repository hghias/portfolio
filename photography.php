<script>

	 $('#photo-nav').toggleClass('navbar-active');

	$(document).ready(function(){
	   
	    $('.sh').addClass("active");
	});

	$('.btn').on('click', function(){
	    $('.btn').removeClass("active");
	    $(this).addClass("active");
	});


	$(".sh").click(function() { //show all
		$(this).toggleClass("active");
		$('div.portrait').show();
		$('div.urban').show();
		$('div.nature').show();
		$('div.wedding').show();
		$('div.event').show();
	});

	$(".cr").click(function() { //creative
		$('div.creative').show();
		$('div.client').hide();
	});

	$(".cl").click(function() { //client
		$('div.client').show();
		$('div.creative').hide();
	});

	$(".pr").click(function() { //portrait
		$(this).toggleClass("active");
		$('div.urban').hide();
		$('div.nature').hide();
		$('div.wedding').hide();
		$('div.event').hide();
		$('div.portrait').show();
	});


	$(".ur").click(function() { //urban
		$(this).toggleClass("active");
		$('div.portrait').hide();
		$('div.nature').hide();
		$('div.wedding').hide();
		$('div.event').hide();
		$('div.urban').show();
	});

	$(".na").click(function() { //nature
		$(this).toggleClass("active");
		$('div.portrait').hide();
		$('div.urban').hide();
		$('div.wedding').hide();
		$('div.event').hide();
		$('div.nature').show();
	});

	$(".we").click(function() { //wedding
		$(this).toggleClass("active");
		$('div.portrait').hide();
		$('div.urban').hide();
		$('div.nature').hide();
		$('div.event').hide();
		$('div.wedding').show();
	});
</script>

